    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int selectedRowIndex = CourseJTable.getSelectedRow();
      
        if(selectedRowIndex < 0)
        {
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            
            return;
        }
      
        DefaultListModel<String> departmentListModel = new DefaultListModel<>();
        JList<String> txtCourseDepartment = new JList<>(departmentListModel);

        txtCourseDepartment.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);

        DefaultTableModel model = (DefaultTableModel)CourseJTable.getModel();
        Course selectedCourse = (Course)model.getValueAt(selectedRowIndex,0);
        
        String department = selectedCourse.getDepartment(); // Assuming this method returns the department as a String
        System.out.println("department"+department);
        departmentListModel.clear(); // Clear the previous data in the list
        departmentListModel.addElement(selectedCourse.getDepartment()); // Add the new department


        int selectedIndex = departmentListModel.indexOf(department);
        System.out.println("selectedIndex"+selectedIndex);
        
        txtCourseName.setText(selectedCourse.getCourseName());
        txtCourseCode.setText(selectedCourse.getCourseCode());
        txtCourseCredit.setText(String.valueOf(selectedCourse.getCredit()));
        if (selectedIndex >=0) {
           txtCourseDepartment.setSelectedIndex(selectedIndex);
        }
        txtCourseDepartment.updateUI();
  
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        
         int selectedRow = CourseJTable.getSelectedRow();
        
        
        if( selectedRow < 0 ){
                JOptionPane.showMessageDialog(jScrollPane1, "Please select a row to update","Warning",JOptionPane.WARNING_MESSAGE);
        
        }else{
             DefaultTableModel model = (DefaultTableModel)CourseJTable.getModel();
             Course selectedCourse = (Course)model.getValueAt(selectedRow,0);
             
             String courseName = txtCourseName.getText();
             String courseCode = txtCourseCode.getText();
             Integer courseCredit = Integer.valueOf(txtCourseCredit.getText());
             String courseDepartment = txtCourseDepartment.getSelectedValue();
 
             
             
             selectedCourse.setCourseCode(courseCode);
             selectedCourse.setCourseName(courseName);
             selectedCourse.setCredit(courseCredit);
             selectedCourse.setDepartment(courseDepartment);
            
        }
        populateTable();
        JOptionPane.showMessageDialog(null, "Course updated successfully");
    }//GEN-LAST:event_jButton3ActionPerformed


    
