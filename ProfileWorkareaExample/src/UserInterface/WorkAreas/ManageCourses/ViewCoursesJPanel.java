 private void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) CourseJTable.getModel();
        dtm.setRowCount(0);
        for(Course course : courselist.getCourseList()){
            Object[] row = new Object[4];
            row[0]=course;
            row[1]= course.getCourseCode();
            row[2] = course.getCredit();
            row[3] = course.getDepartment();
            
            dtm.addRow(row);
        }
    }

