    private void btnSubmitAssignmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitAssignmentActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = jTable1.getSelectedRow();

        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row to view.");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Assignment selectedAssignment = (Assignment) model.getValueAt(selectedRowIndex, 0);

        String assignmentSubmitLink = txtAssignmentSubmitLink.getText();
        if (assignmentSubmitLink.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add submission link");
        } else {
            StudentCourseAssignmentBean.initialize();
            selectedAssignment.addSubmissionLink(profile, assignmentSubmitLink);

            StudentCourseAssignmentBean.saveStudentCourseAssignment(profile, course, selectedAssignment);
            JOptionPane.showMessageDialog(this, "Assignment Submitted Sucessfully");

            System.out.println("*******" + selectedAssignment.getSubmissionLinkStudent(profile));

            displayAssignmentWork();
        }
    }//GEN-LAST:event_btnSubmitAssignmentActionPerformed

