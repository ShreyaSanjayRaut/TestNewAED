    private void displayAssignmentWork() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for (Assignment assignment : courseManagerBean.findAssignmentByCourse(course)) {
            //  List<Course> studentCourses = StudentCourseBean.studentCourseMap.get(student);

            Object[] row = new Object[3];
            row[0] = assignment;

            row[1] = assignment.getSubmissionDate();

            // row[2] = assignment.getStatus();
            row[2] = assignment.getMarksObtained();
            model.addRow(row);
        }

    }
